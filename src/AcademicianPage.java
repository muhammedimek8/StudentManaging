    private void fetchAndShowStudents() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();//table'da veri gösterimi için default table model kullandım
    model.setRowCount(0);//liste güncellenince verilerin silinmesi için ekledim
    String query = "SELECT student_id, name, surname, department FROM students"; 
    try (Connection conn = DriverManager.getConnection(DB_URL, DB_USERNAME, DB_PASSWORD);
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(query)) {
        while (rs.next()) {
            String student_id = rs.getString("student_id");
            String name = rs.getString("name");
            String surname = rs.getString("surname");
            String department = rs.getString("department");
            model.addRow(new Object[]{student_id, name, surname, department});
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}

