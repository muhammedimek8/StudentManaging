    private void select_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_select_btnActionPerformed
        // TODO add your handling code here:
        String selectedOperation=(String)operationComboBox.getSelectedItem();
       if("Select Student".equals(selectedOperation)){
            String searchText = select_txtfield.getText().trim(); // Arama kutusundan isim alınır.
try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection conn = DriverManager.getConnection(DB_URL, DB_USERNAME, DB_PASSWORD);
        String sql = "SELECT student_id, name, surname, department FROM students WHERE name LIKE ?";
        PreparedStatement ptstmt = conn.prepareStatement(sql);
        ptstmt.setString(1, "%" + searchText + "%"); // İsim aramak için LIKE sorgusu kullanılır.
        
        ResultSet rs = ptstmt.executeQuery();
        DefaultListModel<String> model = new DefaultListModel<>(); // Listeyi güncellemek için bir model oluşturulur.
        boolean found = false;
        
        while (rs.next()) {
            found = true;
            String student = "ID: " + rs.getString("student_id") + ",  Name: " + rs.getString("name") + ",  Surname: " + rs.getString("surname") + ",  Department: " + rs.getString("department");
            model.addElement(student); // Her öğrenci için bilgiler listeye eklenir.
        }
        
        if (!found) {
            JOptionPane.showMessageDialog(rootPane, "No student found with the name: " + searchText);
        } else {
            jList1.setModel(model); // JList modeli güncellenir.
        }
        
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, e);
    }
        }
       else{
                   JOptionPane.showMessageDialog(rootPane, "Please choose option again ");

       }
    }//GEN-LAST:event_select_btnActionPerformed

